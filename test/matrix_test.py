import numpy as np
import numpy.linalg as npl


q = np.array([[1, 2, 3], [2, 4, 5], [3, 1, 6], [5, 8, 4]], dtype=np.float)
p = np.array([[1, 2, 3], [2, 4, 5], [3, 1, 6], [5, 8, 4], [3, 6, 1]], dtype=np.float)
q = np.asmatrix(q)
q1 = q + np.array([1, 0, 1])
q2 = q @ np.array([[1, 0, 0], [0, 4/5, -3/5], [0, 3/5, 4/5]])
q3 = q @ np.array([[1, 0, 0], [0, 0, 1], [0, 1, 0]])
q = q - np.average(q, axis=0)
q1 = q1 - np.average(q1, axis=0)
q2 = q2 - np.average(q2, axis=0)
q3 = q3 - np.average(q3, axis=0)
p = p - np.average(p, axis=0)
print(q)
print(q1)
print(q2)
print(q3)
print(p)
